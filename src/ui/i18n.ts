// Internationalization — EN, FR, AR translations.

export type Lang = 'en' | 'fr' | 'ar';
export const LANGS: Lang[] = ['en', 'fr', 'ar'];
export const LANG_LABELS: Record<Lang, string> = { en: 'English', fr: 'Français', ar: 'العربية' };

let currentLang: Lang = 'fr';

export function getLang(): Lang { return currentLang; }
export function setLang(lang: Lang): void { currentLang = lang; }
export function isRTL(): boolean { return currentLang === 'ar'; }

export function getLocale(): string {
  return currentLang === 'ar' ? 'ar-SA' : currentLang === 'fr' ? 'fr-FR' : 'en-US';
}

